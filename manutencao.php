                $mtbf=0;

                $mttr=0;

                $dispManut=1;

                $downtime=0;
                
                $resposta="Não houveram eventos de manutenção reportados! Total disponibilidade para os equipamentos produtivos!";
                
                $imagem="Bom";
                
            } else{
                $tempoTotalOperação=$dias*$horas;

                $tempoTrabalhado=$tempoTotalOperação-$quebra/60;

                $mtbf=$tempoTrabalhado/$numQuebra;

                $mttr=(($quebra/60)/$numQuebra);

                $dispManut=$mtbf/($mtbf+$mttr);

                $downtime=($quebra/60)/$tempoTotalOperação;
                
                if($dispManut<0.9){
                $resposta="Seu indicador de Disponibilidade está abaixo do ideal! Podemos auxiliar você a melhorar seus resultados! Clique no link abaixo para saber como nos contatar!";
